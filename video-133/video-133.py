#   Division entera //
numero = 5
numero1 = 2
division = numero / numero1
print(
    f"La variable numero tiene {numero} y numero1 {numero1} la division da {division}"
)
division_entera = numero // numero1
print(
    f"La variable numero tiene {numero} y numero1 {numero1} la division entera da {division_entera}"
)
