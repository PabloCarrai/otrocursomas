#   Operadores de asignacion
print("Operadores de asignacion xor ^")
numero = 10
print("Contenido de la variable numero ", numero)
numero ^= 2
print("Contenido de la variable numero ", numero)
numero = 10
numero >>= 1
print("Contenido de la variable numero ", numero)
numero <<= 1
print("Contenido de la variable numero ", numero)
