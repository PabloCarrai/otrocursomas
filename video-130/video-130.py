#   Division entre variables y literales numericas reales
print("Division entre variables y literales numericas reales")
numero = 0.5
numero1 = 1.73
division = numero / numero1
print(f"La division de {numero} y {numero1} es igual a {division}")
#   Variable y literal
print("Variable y literal")
division1 = numero / 1.73
print(f"La division de {numero} y {1.73} es igual a {division1}")
# Division entre dos literales
print("Division entre dos literales")
print(f"La division de {6.456} y {1.23} es igual a {6.456/1.23}")
# Division entre multiples literales
division2 = 22.3 / 3.7 / 1.32 / 7.6
print(f"La division de 22.3 / 3 / 2 / 7.6 es igual a {division2}")
