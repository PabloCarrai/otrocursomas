#   Convertir una lista en una tupla
lista=[1,2,3,4,5,6,7]
print(f"Tengo la lista {lista}")
print(f"Para desconfiados es del tipo {type(lista)}")
tuplaProvenientedeLista=tuple(lista)
print(f"Tengo una tupla a partir de una lista {tuplaProvenientedeLista}")
print(f"Para desconfiados es del tipo {type(tuplaProvenientedeLista)}")
