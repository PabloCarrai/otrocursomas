#   Operadores de asignacion
print("Operadores de asignacion producto *=")
numero = 10
print("Contenido de la variable numero ", numero)
numero *= 3  # asignacion producto *=
print("Contenido de la variable numero ", numero)
numero /= 3  # asignacion division /=
print("Contenido de la variable numero ", numero)
numero //= 3  # asignacion division entera //=
print("Contenido de la variable numero ", numero)
