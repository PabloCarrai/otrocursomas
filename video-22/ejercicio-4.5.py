#   Calcular el area de un triangulo dada la base y la altura
#   area=(base * altura)/2
base = float(input("La base del triangulo es?  "))
altura = float(input("La altura del triangulo es?  "))
area = (base * altura) / 2
print(f"El triangulo tiene una base de {base}, su altura es {altura} y su area {area}")
