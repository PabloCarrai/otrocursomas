#   Operador logico de negacion not
print("Operador logico de negacion not")
llueve = True
print(f"Contenido de la variable llueve {llueve}")
llueve = not llueve  # Negamos el valor de llueve
print(f"Contenido de la variable llueve {llueve}")
edad = 17
resultado = not edad < 18  # Negamos el resultado de la operacion
print(f"Valor de resultado es {resultado}")
edad = 19
resultado = not edad < 18  # Negamos el resultado de la operacion
print(f"Valor de resultado es {resultado}")