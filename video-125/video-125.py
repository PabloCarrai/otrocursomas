#   Resta de numeros de punto flotantes
print("Resta de numeros de punto flotantes")
numero_1 = 3.1415
numero_2 = 2.7172
resta = numero_1 - numero_2
print(f"La resta de numero_1={numero_1} y numero_2={numero_2} es igual a {resta}")
#   Resta valores reales como literales
print("Resta valores reales como literales")
resta_1 = 3.1415 - 2.7172
print(f"La resta de 3.1415-2.72172 da {resta_1} ")
# Resta entre variable y literal
print("Resta entre variable y literal")
resta_2 = numero_1 - 2.7172
print(f"La resta de numero_1={numero_1} - 2.72172 da {resta_2} ")
