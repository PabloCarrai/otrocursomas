# Operador de diferencia -
numero_1 = 5
numero_2 = 3
resta = numero_1 - numero_2
print(f"La resta de numero_1={numero_1} y numero_2={numero_2} es igual a {resta}")
# con valores literales
resta_1 = 5 - 3
print("Con literales")
print(f"La resta de {5} y {3} es igual a {resta_1}")
#   Restar variable y literal
print("Restar variable y literal")
resta_2 = numero_1 - 3
print(f"La resta de numero_1 {numero_1} y {3} es igual a {resta_2}")
#   Restar multiples valores
print("Restar multiples valores")
resta_3 = 5 - 3 - 2 - 4 - 1
print(f"La operacion 5-3-2-4-1 da {resta_3}")
