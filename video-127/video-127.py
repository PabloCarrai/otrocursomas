#   Producto    *

print("Producto *")
numero1 = 3
numero2 = 7
producto = numero1 * numero2
print(f"El producto entre numero1={numero1} y numero2={numero2} es {producto}")
# Multiplicar dos literales
print("Multiplicar dos literales")
producto1 = 3 * 7
print(f"Multiplicamos 3 * 7 = {producto1}")
# Multiplicar una variable por una literal numerica
print("Multiplicar una variable por una literal numerica")
producto2 = numero1 * 7
print(f"Multiplicamos numero1={numero1} * 7 = {producto2}")
# Multiplicar multiples valores
producto3 = 7 * 3 * 21 * 4 * 5 * 6 * 1 * 7
print(f"Multiplicamos 7 * 3 * 21 * 4 * 5 * 6 * 1 * 7 que es  {producto3}")
