#   Division /  (genera decimales)

numero1 = 1
numero2 = 2
division = numero1 / numero2
print(f"Division entre numero1 = {numero1} y numero2 = {numero2} es igual a {division}")
# Division entre dos literales
print("Division entre dos literales")
division1 = 1 / 2
print(f"La division entre dos literales 1 / 2 es igual a {division1}")
# Division entre una variable y un literal
print("Division entre una variable y un literal")
division2 = 1 / numero2
print(
    f"La division entre un literales 1 / una variable numero2= {numero2} es igual a {division2}"
)
#   Division entre varios operandos
print("Division entre varios operandos")
division3 = 8 / 4 / 3 / 2
print(f"Division entre varios operando  8 / 4 / 3 / 2 = {division3}")
