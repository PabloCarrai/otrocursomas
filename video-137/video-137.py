#   Operador de Mayor que >
print("Operadores de comparacion  Mayor >")
numero1 = 5
numero2 = 3
resultado = numero1 > numero2
print(f"{numero1} es Mayor a {numero2} {resultado}")
numero3 = 7
print(f"{numero1} es Mayor a {numero3} {numero1>numero3}")
numero1 = 1.1
numero2 = 1.11
resultado = numero1 > numero2
print(f"{numero1} es Mayor a {numero2} {resultado}")
