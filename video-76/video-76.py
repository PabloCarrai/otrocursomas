#   Diccionarios
#   1 forma de crear un diccionario
diccionario_1 = {"propiedad1": 1, "propiedad2": 2, "propiedad3": [1, 2, 3]}
#   2 forma de crear un diccionario
diccionario_2 = dict()
diccionario_2["propiedad1"] = 1
diccionario_2["propiedad2"] = 2
diccionario_2["propiedad3"] = [1, 2, 3]
#   Mostrar las representaciones
print(f"El primer diccionario luce {diccionario_1}")
print(f"El tipo de dato del diccionario diccionario_1 es {type(diccionario_1)}")
print(f"El segundo diccionario luce {diccionario_2}")
print(f"El tipo de dato del diccionario diccionario_2 es {type(diccionario_2)}")
