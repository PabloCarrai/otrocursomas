#   Precedencia Jerarquica de Operadores en expresion aritmetica
print("Precedencia Jerarquica de Operadores")
resultado = 5 / 2 + 4
print(f"El contenido de resultado es {resultado}")
print("Mismo resultado cambiando orden de operadores")
resultado = 4 + 5 / 2
print(f"El contenido de resultado es {resultado}")
print("Cambiando la operacion")
resultado = 5 / (2 + 4)
print(f"El contenido de resultado es {resultado}")
